// 获取所有唯一标签
function getAllTags() {
    const allTags = new Set();
    articlesData.forEach(article => {
        article.tags.forEach(tag => allTags.add(tag));
    });
    return Array.from(allTags).sort();
}

// 根据标签筛选文章
function filterArticlesByTag(tag) {
    if (tag === 'all') {
        return articlesData;
    }
    return articlesData.filter(article => article.tags.includes(tag));
}

// 渲染文章列表
function renderArticleList(articles) {
    const articleList = document.getElementById('article-list');
    articleList.innerHTML = '';
    
    articles.forEach(article => {
        const articleItem = document.createElement('div');
        articleItem.className = 'article-item';
        articleItem.innerHTML = `
            <h3>${article.title}</h3>
            <div class="tags">
                ${article.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
            </div>
            <p class="preview">${article.content.substring(0, 100)}...</p>
        `;
        articleItem.addEventListener('click', () => showArticleDetail(article));
        articleList.appendChild(articleItem);
    });
}

// 显示文章详情
function showArticleDetail(article) {
    const detailView = document.getElementById('article-detail');
    const listView = document.getElementById('article-list-view');
    
    document.getElementById('detail-title').textContent = article.title;
    document.getElementById('detail-content').textContent = article.content;
    document.getElementById('detail-tags').innerHTML = 
        article.tags.map(tag => `<span class="tag">${tag}</span>`).join('');
    
    listView.style.display = 'none';
    detailView.style.display = 'block';
}

// 返回文章列表
function backToList() {
    document.getElementById('article-detail').style.display = 'none';
    document.getElementById('article-list-view').style.display = 'block';
}

// 渲染标签筛选器
function renderTagFilter() {
    const tagFilter = document.getElementById('tag-filter');
    const tags = getAllTags();
    
    tagFilter.innerHTML = `
        <button class="tag-btn active" data-tag="all">全部</button>
        ${tags.map(tag => `<button class="tag-btn" data-tag="${tag}">${tag}</button>`).join('')}
    `;
    
    // 添加标签筛选事件
    tagFilter.addEventListener('click', (e) => {
        if (e.target.classList.contains('tag-btn')) {
            // 更新活动状态
            document.querySelectorAll('.tag-btn').forEach(btn => btn.classList.remove('active'));
            e.target.classList.add('active');
            
            // 筛选并显示文章
            const selectedTag = e.target.dataset.tag;
            const filteredArticles = filterArticlesByTag(selectedTag);
            renderArticleList(filteredArticles);
        }
    });
}

// 页面初始化
function initPage() {
    renderTagFilter();
    renderArticleList(articlesData);
}

// 当DOM加载完成后初始化页面
document.addEventListener('DOMContentLoaded', function() {
    initPage();
});

// 导出函数供HTML使用
window.backToList = backToList;